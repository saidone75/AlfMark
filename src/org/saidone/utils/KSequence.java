package org.saidone.utils;

public class KSequence {

	private static KSequence instance = null;
	protected static int i = 0;

	private KSequence() {
	}

	public static synchronized int next() {
		if (instance == null) {
			instance = new KSequence();
		}
		i++;
		return i;
	}

}
